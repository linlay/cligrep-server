USE `cligrep`;

CREATE TABLE IF NOT EXISTS cli_locale_content (
  CLI_SLUG_ VARCHAR(128) NOT NULL,
  LOCALE_ VARCHAR(32) NOT NULL,
  DISPLAY_NAME_ VARCHAR(255) NULL,
  SUMMARY_ TEXT NULL,
  HELP_TEXT_ MEDIUMTEXT NULL,
  TAGS_JSON_ JSON NULL,
  CREATED_AT_ DATETIME(3) NOT NULL,
  UPDATED_AT_ DATETIME(3) NOT NULL,
  PRIMARY KEY (CLI_SLUG_, LOCALE_),
  KEY IDX_CLI_LOCALE_CONTENT_LOCALE (LOCALE_),
  CONSTRAINT FK_CLI_LOCALE_CONTENT_CLI FOREIGN KEY (CLI_SLUG_) REFERENCES cli_registry (SLUG_) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
